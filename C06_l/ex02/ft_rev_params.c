#include <unistd.h>

int	main(int argc, char **argv)
{
	int	j;

	while (argc > 1)
	{
		argc--;
		j = -1;
		while (argv[argc][++j] != 0)
			write(1, &(argv[argc][j]), 1);
		write(1, "\n", 1);
	}
}
